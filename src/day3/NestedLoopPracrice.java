package day3;

public class NestedLoopPracrice {

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) { // i=4
            for (int j = 0; j < i; j++) {   //J=0
                System.out.print("*");
            }
            System.out.println();
        }
    }
}


