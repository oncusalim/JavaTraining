package tasks.day1;

public class Task2 {

    public static void main(String[] args)
    {
        int yas = 23;

        String isim = "Ali";
        String sehir = "İstanbul";

        System.out.println("Adım: " + isim + " " + "Yaşım: " + yas);
        System.out.println(yas);
        System.out.println("Şehrim: " + sehir);
        System.out.print(isim);
    }
}

