package tasks.day1;

public class Task7 {

    public static void main(String[] args)
    {
        int sayi = 6;

        sayi += 50;

        System.out.println("Sayı : " + sayi);
    }
}


/*
= Açıklama =
Java'da aritmetik operatörleri bir sayının 50 fazlasını aynı değere atayıp hesaplayan bir program yazın.

Not: Java'da "int" bir değişken tanımlayalım.
     Sayı "6" olsun.
     Bu değerin 50 fazlasını değişkene geri atayan Java kodunu yazınız.

İpucu: +=
*/